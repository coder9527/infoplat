package com.info.repertory.model;

public class Repertory {
	  private  String id;
	     
		
      private  String productName;
     
	
      private  String productNo;
     
	
      private  String countNum;
     
	
      private  String repertoryNum;
     
	
      private  String corperatrion;
      
      
      private String picUrl;
     
      
      public String getPicUrl() {
		return picUrl;
	}
	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}
	public  String  getId(){
      		return  this.id;
      };
      public  void  setId(String id){
      	this.id=id;
      }  
     
      public  String  getProductName(){
      		return  this.productName;
      };
      public  void  setProductName(String productName){
      	this.productName=productName;
      }  
     
      public  String  getProductNo(){
      		return  this.productNo;
      };
      public  void  setProductNo(String productNo){
      	this.productNo=productNo;
      }  
     
      public  String  getCountNum(){
      		return  this.countNum;
      };
      public  void  setCountNum(String countNum){
      	this.countNum=countNum;
      }  
     
      public  String  getRepertoryNum(){
      		return  this.repertoryNum;
      };
      public  void  setRepertoryNum(String repertoryNum){
      	this.repertoryNum=repertoryNum;
      }  
     
      public  String  getCorperatrion(){
      		return  this.corperatrion;
      };
      public  void  setCorperatrion(String corperatrion){
      	this.corperatrion=corperatrion;
      }  

}
